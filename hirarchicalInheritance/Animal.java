package com.hirarchicalInheritance;

public class Animal {
	String animalName;
	public void sleep()
	{
		System.out.println(animalName+" is sleeping");
	}
	public void eat()
	{
		System.out.println(animalName+" is Eating");
	}
	public void run()
	{
		System.out.println(animalName+" is Running");
	}
}
